/**
 * while循环
 * while（循环条件）{
 * 		循环内容
 * }
 * 循环条件为bool值，满足条件执行内容，当不满足条件时，不执行循环
 * @author jjking
 *
 */
public class Exercise01 {
	public static void main(String[] args) {
		int i=1;
		do {
			System.out.print(i+"\t");
			if (i%10==0) {
				System.out.print("\n");
			}
			i++;
		} while (i<=100);                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                          
	}
}
                                                                                                                                                                                                                                                                                                                                                                                                                                                                                         